namespace TowARDefense.Menus
{
    abstract public class Entry
    {
        public bool selected;
        public event EventHandler handler;

        public Entry(bool selected_f)
        {
            selected = selected_f;
        }

        public void pressed()
        {
            if (handler != null)
                handler(this, null);
        }
    }
}
